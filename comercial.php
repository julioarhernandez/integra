					<h3>﻿Tipos de seguros necesarios para un negocio</h3>
					<p> <h5>Seguro de vehículos de la empresa</h5>
Un vehículo, ya sea un coche o un camión, que se utiliza para actividades de negocios también necesita una cobertura de seguro adecuada. Por ejemplo, las empresas de alquiler de vehículos deben contratar una cobertura antes de alquilar vehículos a los clientes para proteger el vehículo. Las empresas de transportes deben proporcionar un seguro a los camiones y remolques involucrados en actividades comerciales.

<h5>Seguro de la propiedad comercial</h5>
Una póliza de seguro estándar de la propiedad comercial contiene cobertura de la propiedad de la empresa, como el edificio de la empresa o los equipos utilizados durante las operaciones. Por ejemplo, si un incendio o un desastre natural daña la oficina de una empresa, el seguro de propiedad cubrirá habitualmente este caso.

<h5>Seguro de responsabilidad civil de la empresa</h5>
El seguro de responsabilidad civil de la empresa cubre las pérdidas experimentadas por las personas que frecuentan el negocio. Por ejemplo, si un cliente resulta herido en un accidente de resbalón y caída mientras está en el establecimiento comercial, el seguro de responsabilidad civil puede cubrir el costo. Algunas compañías de seguros ofrecen paquetes abarcando propiedades, vehículos y seguro de responsabilidad civil en una póliza llamada Póliza de Dueño de Negocios, o BOP (por sus siglas en inglés).

<h5>Compensación a los trabajadores y seguro de desempleo</h5>
Muchos estados requieren que las empresas que contratan trabajadores se aseguren con pólizas de seguro de compensación al trabajador. Este seguro le paga al trabajador en caso de un accidente en el entorno laboral. Este seguro es común en los puestos de trabajo que requieren mano de obra o actividad física. Muchos estados también requieren una cobertura de seguro de desempleo para los trabajadores. El estado es quien generalmente maneja la póliza, mientras que los empleadores pagan un impuesto sobre el empleo para cubrir el costo.

<h5>Seguro de errores y omisiones</h5>
Un seguro de errores y omisiones, también llamado seguro de responsabilidad profesional, cubre las pérdidas causadas por los profesionales de negocios que cometen errores o descuidos en la realización de sus trabajos. Por ejemplo, si un consultor no informa a un cliente de una importante información en relación con un proyecto que causa la pérdida de ingresos, este tipo de póliza de seguro puede pagar por la pérdida. Los abogados, médicos y arquitectos se encuentran entre los tipos de profesionales que a veces compran este tipo de seguro.</p>
					
